#ifndef __ASM_RTL8196E_H
#define __ASM_RTL8196E_H

#include <linux/compiler.h>

#define RTL8196E_REG_BASE ((void __iomem *)0xB8000000)
#define RTL8196E_REGISTER(offset) (RTL8196E_REG_BASE + offset)

/* Watchdog registers */
#define RTL8196E_CDBR        RTL8196E_REGISTER(0x3118)
#define RTL8196E_WDTCNR      RTL8196E_REGISTER(0x311C)

/* UART addresses */
#define RTL8196E_UART0_BASE  RTL8196E_REGISTER(0x2000)
#define RTL8196E_UART1_BASE  RTL8196E_REGISTER(0x2100)

/* GPIO registers */
#define RTL8196E_GPABCDCNR   RTL8196E_REGISTER(0x3500)
#define RTL8196E_GPABCDTYPE  RTL8196E_REGISTER(0x3504)
#define RTL8196E_GPABCDDIR   RTL8196E_REGISTER(0x3508)
#define RTL8196E_GPABCDDATA  RTL8196E_REGISTER(0x350C)
#define RTL8196E_GPABCDISR   RTL8196E_REGISTER(0x3510)
#define RTL8196E_GPABIMR     RTL8196E_REGISTER(0x3514)
#define RTL8196E_GPCDIMR     RTL8196E_REGISTER(0x3518)
#define RTL8196E_GPEFGHCNR   RTL8196E_REGISTER(0x351C)
#define RTL8196E_GPEFGHTYPE  RTL8196E_REGISTER(0x3520)
#define RTL8196E_GPEFGHDIR   RTL8196E_REGISTER(0x3524)
#define RTL8196E_GPEFGHDATA  RTL8196E_REGISTER(0x3528)
#define RTL8196E_GPEFGHISR   RTL8196E_REGISTER(0x352C)
#define RTL8196E_GPEFIMR     RTL8196E_REGISTER(0x3530)
#define RTL8196E_GPGHIMR     RTL8196E_REGISTER(0x3534)


#endif /* __ASM_RTL8196E_H */
